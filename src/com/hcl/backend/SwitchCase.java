package com.hcl.backend;

public class SwitchCase {

	public static void main(String[] args) {
		// giving an example of using switch and case 
		
		String timeOfDay = "Morning";
		
		switch(timeOfDay){
		
		 case  "Afternoon":
			System.out.println("What's for Lunch?");
		
		 case "Dinner":
			 System.out.println("What's for Dinner?");
			
	     default:
	    	 System.out.println("Time for Breakfast!");
			
		
		
		
		}
	};

}
